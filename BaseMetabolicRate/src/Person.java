public class Person {
	int altezza;
	int peso;
	int eta;
	String attivitaf;
	char sesso;
	
	public Person(int h, int w, int a, String attfis, char s){
		this.altezza = h;
		this.peso = w;
		this.eta = a;
		this.attivitaf = attfis;
		this.sesso = s;
	}
	public Person(){
		this.altezza = 0;
		this.peso = 0;
		this.eta = 0;
		this.attivitaf = null;
		this.sesso = ' ';
	}
	
}

	
	
	

