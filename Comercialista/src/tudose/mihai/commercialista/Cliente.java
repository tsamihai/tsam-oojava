package tudose.mihai.commercialista;

public class Cliente {
	private String dipendenti, liberiProfessionisti, imprenditori;

	public String getDipendenti() {
		return dipendenti;
	}

	public void setDipendenti(String dipendenti) {
		this.dipendenti = dipendenti;
	}

	public String getLiberiProfessionisti() {
		return liberiProfessionisti;
	}

	public void setLiberiProfessionisti(String liberiProfessionisti) {
		this.liberiProfessionisti = liberiProfessionisti;
	}

	public String getImprenditori() {
		return imprenditori;
	}

	public void setImprenditori(String imprenditori) {
		this.imprenditori = imprenditori;
	}
	
	

}
