package tudose.mihai.harbour;

public class PortoNavale {
	 private String nome;
	 private  int NumeroImbarcazioni;
	 private  int Dimensione;
	 private  int MquaRimasti;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getNumeroImbarcazioni() {
		return NumeroImbarcazioni;
	}
	public void setNumeroImbarcazioni(int numeroImbarcazioni) {
		NumeroImbarcazioni = numeroImbarcazioni;
	}
	public int getDimensione() {
		return Dimensione;
	}
	public void setDimensione(int dimensione) {
		this.Dimensione = dimensione;
	}
	public int getMquaRimasti() {
		return MquaRimasti;
	}
	public void setMquaRimasti(int mquaRimasti) {
		MquaRimasti = mquaRimasti;
	}

	 
}
